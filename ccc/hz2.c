#include<stdio.h>

int main() {
	int i, a[200000] = {0};
	a[1] = 1;
	a[2] = 1;
	int s = 0;
	for(i = 3; a[i - 1] <= 4000000; i++) {
		a[i] = a[i - 1] + a[i - 2];
		if(a[i] % 2 == 0) {
			s += a[i];
		}
	}
	printf("%d", s);
	return 0;
}
