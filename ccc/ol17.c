#include<stdio.h>

int main() {
	int s;
	int aa[8] = {6, 6, 6, 5, 5, 7, 6, 5};//20 ～ 70的头
	int a[19] = {3, 3, 5, 4, 4, 3, 5, 5, 4, 3, 6, 6, 8, 8, 7, 7, 9, 8, 8};//前19个
	int b[9] = {0};
	int s9 = 36;
	int s19 = 0;
	for(int i = 0; i < 19; i++) {
		s19 += a[i];
	}
	int s99 = s19;
	for (int i = 0; i <= 7; i++) {
		s99 += aa[i] * 10 + s9;
	}
	for(int i = 0; i < 9; i++) {
		b[i] = a[i] * 100 + 3 * 99 + s99 + 100 * 7;
	}
	s = 11 + s99;
	for(int i = 0; i < 9; i++) {
		s += b[i];
		printf("%d\n", s);
	}
	printf("%d\n", s);
}
