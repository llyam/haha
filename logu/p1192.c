#include<stdio.h>

int main() {
    int x, y, f[100000] = {1};
    scanf("%d %d", &x, &y);
    for (int i = 1; i <= x; i++) {
        for (int j = 1; j <= y && i - j >= 0; j++) {
            f[i] += f[i - j];
        }
        f[i] %= 100003;
    }
    printf("%d\n", f[x]);
}
